class avaRndAdd:
    def __init__(self):
        self.a = 0
        self.b = 0
        self.result = 0

    def add_numbers(self, a, b):
        self.a = a
        self.b = b
        self.result = self.a + self.b

        return self.result
    
class avaRndSubtract:
    def __init__(self):
        self.a = 0
        self.b = 0
        self.result = 0

    def add_numbers(self, a, b):
        self.a = a
        self.b = b
        self.result = self.a - self.b

        return self.result